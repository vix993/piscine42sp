int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned char c1;
	unsigned char c2;

	if (n == 0)
		return (0);
	c1 = '\0';
	c2 = '\0';
	while (--n > 0)
	{
		c1 = *s1++;
		c2 = *s2++;
		if (c1 == '\0' || c1 != c2)
			return (c1 - c2);
	}
	return (c1 - c2);
}
