int		ft_sqrt(int nb)
{
	int sqrt;

	sqrt = 1;
	while (sqrt * sqrt < nb)
	{
		sqrt += 1;
	}
	if (sqrt == (nb / sqrt))
		return (sqrt);
	return (0);
}
